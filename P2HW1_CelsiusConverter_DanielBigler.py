# input is in celsius
degrees_celsius = float( input('enter the temperature in Celsius: '))

# Calculate from celsius to fahrenheit
degrees_fahrenheit = (degrees_celsius * 1.8) + 32

# Display the temperature in fahrenheit
print('The temperature in Fahrenheit is ', format (degrees_fahrenheit))
